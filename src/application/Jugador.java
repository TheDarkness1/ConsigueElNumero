package application;

public class Jugador {
	
	private String nombre;
	private int puntaje;
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public void setPuntaje(int puntaje) {
		this.puntaje = puntaje;
	}
	
	public String getNombre() {
		return nombre;
	}
	
	public int getPuntaje() {
		return puntaje;
	}
	
}
